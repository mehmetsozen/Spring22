package home;

public class For_For {
    public static void main(String[] args) {
        int week=4;
        int day=7;
        for(int i=1;i<=week;i++){
            System.out.println("Week : "+i);
            for(int k=1;k<=day;k++) {
                System.out.println("      Day : " + k);
            }

            }

        }

    }

